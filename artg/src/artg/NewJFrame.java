    private void usearchiconActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_usearchiconActionPerformed
        try
        {
           if(!usearch.getText().equals("") && !usearch.getText().equals("Search") )
           {
              stmt=conn.createStatement();
            String stitle=usearch.getText();
            String sql;
            sql = "SELECT *FROM data WHERE title='"+stitle+"';";
            rs=stmt.executeQuery(sql);
            if(rs.next())
            {
                utitle.setText(rs.getString("title"));
                uartist.setText(rs.getString("artist"));
                utype.setText(rs.getString("type"));
                ustatus.setText(rs.getString("status"));
                upaint.setText(String.format("%s", rs.getInt("paintno")));
                uprice.setText(String.format("%s", rs.getInt("price")));
                uurl.setText("/painting/"+utype.getText()+"/"+utitle.getText()+".jpg");
                uphoto.setIcon(new javax.swing.ImageIcon(getClass().getResource("/paintings/"+utype.getText()+"/"+utitle.getText()+".jpg")));
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Painting not found");
            } 
           }
           else
               JOptionPane.showMessageDialog(null,"Null cann't be searched");
            
        } 
        catch (SQLException ex) 
        {
            Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_usearchiconActionPerformed

